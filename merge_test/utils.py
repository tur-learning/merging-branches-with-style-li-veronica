import math
#this is an already existing python module

def add(a,b):
    """
    Adds two numbers
    """
    res = a + b
    return res

def multiply(a,b):
    """
    Multiples two numbers
    """
    res = a * b
    return res

def sqrt(a):
    """
    Calculates sqrt using built in python thingy
    """
    res = math.sqrt(a)
    return res

